// Function to get full blog content from markdown files
export async function getBlogPostContent(lang: 'en' | 'zh', id: string) {
  try {
    const file = await import(`../content/blog/${lang}/${id}.md?raw`);
    const rawContent = file.default;

    // Extract frontmatter
    const frontmatterMatch = rawContent.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
    if (frontmatterMatch) {
      const frontmatterText = frontmatterMatch[1];
      const markdownContent = frontmatterMatch[2];

      // Parse simple frontmatter
      const metadata: any = {};
      const frontmatterLines = frontmatterText.split('\n');
      frontmatterLines.forEach((line: string) => {
        const match = line.match(/^(\w+):\s*"?(.*?)"?$/);
        if (match) {
          metadata[match[1]] = match[2];
        }
      });

      // Convert markdown to HTML
      const htmlContent = markdownToHtml(markdownContent);

      return {
        metadata,
        content: htmlContent
      };
    }

    return null;
  } catch (error) {
    console.error(`Error loading blog content for ${lang}/${id}:`, error);
    return null;
  }
}

// Simple markdown to HTML converter
function markdownToHtml(markdown: string): string {
  let html = markdown;

  // Headers
  html = html.replace(/^### (.*$)/gim, '<h3 id="$1">$1</h3>');
  html = html.replace(/^## (.*$)/gim, '<h2 id="$1">$1</h2>');
  html = html.replace(/^# (.*$)/gim, '<h1>$1</h1>');

  // Bold
  html = html.replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');

  // Italic
  html = html.replace(/\*(.*?)\*/g, '<em>$1</em>');

  // Code blocks
  html = html.replace(/```(\w+)?\n([\s\S]*?)```/g, '<pre><code>$2</code></pre>');

  // Inline code
  html = html.replace(/`([^`]+)`/g, '<code>$1</code>');

  // Blockquotes
  html = html.replace(/^> (.*$)/gim, '<blockquote>$1</blockquote>');

  // Unordered lists
  html = html.replace(/^\s*-\s+(.*$)/gim, '<li>$1</li>');
  html = html.replace(/(<li>.*<\/li>)/g, '<ul>$1</ul>');
  html = html.replace(/<\/ul>\s*<ul>/g, '');

  // Ordered lists
  html = html.replace(/^\s*\d+\.\s+(.*$)/gim, '<li>$1</li>');

  // Links
  html = html.replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2">$1</a>');

  // Images
  html = html.replace(/!\[([^\]]*)\]\(([^)]+)\)/g, '<img src="$2" alt="$1">');

  // Paragraphs
  html = html.replace(/^(?!<[a-z]|<li|<ul|<h[1-6]|<blockquote|<pre)(.*$)/gim, '<p>$1</p>');
  html = html.replace(/<p>\s*<\/p>/g, '');

  // Clean up empty paragraphs
  html = html.replace(/<p>\s*(<.*?>)\s*<\/p>/g, '$1');

  return html;
}
